//숫자
//함수
let num = 10;
console.log(num);
console.log(typeof num);

//실수
let num2 = 3.14;
console.log(num2);
console.log(typeof num2);

//지수형
let num3 = 2.5e5; // 2.5 x 10^5
console.log(num3);
console.log(typeof num3);

//
//NAN = not number
let num4 = "hello" / 2;
console.log(num4);

//infinity
let num5 = 1 / 0;
console.log(num5);
console.log(typeof num5);

//infinity
let num6 = -1 / 0;
